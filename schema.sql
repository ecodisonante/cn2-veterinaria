------------------------------------------------------------
-- Vistas útiles
------------------------------------------------------------
CREATE OR REPLACE VIEW VW_CLIENTE_DEUDA AS
SELECT f.CLIENTE_ID,
       SUM(CASE WHEN (SELECT e.NOMBRE FROM ESTADO e
                      JOIN FACTURA_ESTADO fe ON fe.ESTADO_ID = e.ID
                      WHERE fe.ESTADO_ID = f.ESTADO_ID) = 'PAGADA'
                THEN 0 ELSE f.TOTAL END)
         - COALESCE((SELECT SUM(p.MONTO) FROM PAGO p WHERE p.FACTURA_ID = f.ID), 0) AS DEUDA
FROM FACTURA f
GROUP BY f.CLIENTE_ID;

------------------------------------------------------------
-- Trigger de inventario (descuento por atención)
------------------------------------------------------------
CREATE OR REPLACE TRIGGER TRG_AI_DESCUENTO
AFTER INSERT ON ATENCION_ITEM
FOR EACH ROW
BEGIN
  UPDATE INVENTARIO_ITEM
     SET STOCK_ACTUAL = STOCK_ACTUAL - :NEW.CANTIDAD
   WHERE ID = :NEW.ITEM_ID;

  INSERT INTO INVENTARIO_MOV (ITEM_ID, TIPO_MOV, CANTIDAD, MOTIVO, REFERENCIA_ID)
  VALUES (:NEW.ITEM_ID, 'EGRESO', :NEW.CANTIDAD, 'Uso en atención', :NEW.ATENCION_ID);
END;
